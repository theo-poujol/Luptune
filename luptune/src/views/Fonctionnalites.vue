import Contact from '../components/Contact';

export default {

  metaInfo() {
    return {
      title: "Luptune - Fonctionnalités",
      meta: [
        {
          name: "Luptune - Découvrez nos fonctionnalités",
          content: "Commandes en ligne et click & collect, gestion des clients et réservations. Découvrez le large choix qui s'offre à vous."
        }
      ]
    }
  },

  created () {

  },


  data() {

    return {
      fadeInElements  : [],
    }
  },

  mounted() {

    this.fadeInElements = Array.from(document.getElementsByClassName('fade-in'))

    document.addEventListener('scroll', this.handleScroll)
  },

  unmounted() {
    document.removeEventListener('scroll', this.handleScroll)
  },

  components: {
    Contact
  },

  methods: {
    getLocaleDateString() {
      var s = new Date().toLocaleDateString();
      var sp = s.split(',');
      var sp2 = sp[0].split('/');
      var d = sp2[2].concat('-', sp2[1], '-', sp2[0]);
      console.log(d.toString());
      return d.toString();
    },

    isElemVisible : function (el) {
      const rect = el.getBoundingClientRect()
      const elemTop = rect.top + 200; // buffer
      const elemBottom = rect.bottom;
      return elemTop < window.innerHeight && elemBottom >= 0;
    },

    handleScroll: function () {
      for (let i = 0; i < this.fadeInElements.length; i++) {
          const elem = this.fadeInElements[i]
          if (this.isElemVisible(elem)) {
              elem.style.opacity = '1'
              elem.style.transform = 'scale(1)'
              elem.style.transition = '1.25s'
              this.fadeInElements.splice(i, 1) // Pour jouer l'animation seulement une fois
          }
      }
    }
  },

}
